/**
 * A class that shows the how to change integers to floating points before floating-point operations
 * DCL52-J: Do not declare more than one variable per declaration
 * Created by Ryan Leone
 * IT 355
 */
public class OneVariablePerDeclaration
{
    /**
     * Main method that shows how one variable per declaration is more readable
     * @param args  command line arguments provided by the user
     */
    public static void main(String[] args)
    {
        /**
         * By placing one declaration on each line it easily visible which variables have what default value
         *      and what type they are
         */
        int i = 1;
        int j = 1;
        int k = 0;
        int[] l;
    }
}
